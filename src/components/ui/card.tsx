import * as React from "react"

import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    variant?: 'default' | 'glass' | 'elevated'
  }
>(({ className, variant = 'default', ...props }, ref) => {
  const variants = {
    default: "rounded-xl border bg-card text-card-foreground shadow-card",
    glass: "rounded-xl border bg-card/80 backdrop-blur-sm text-card-foreground shadow-card",
    elevated: "rounded-xl border bg-card text-card-foreground shadow-card hover:shadow-card-hover transition-shadow duration-300",
  }

  return (
    <div
      ref={ref}
      className={cn(variants[variant], className)}
      {...props}
    />
  )
})
Card.displayName = "Card"

