const generateReviews = (positive: number, neutral: number, negative: number): Review[] => {
  const reviews: Review[] = [];
  
  const positiveTexts = [
    "Amazing food and great service! Highly recommend.",
    "Best restaurant in Phnom Penh. Will definitely come back.",
    "Delicious dishes at reasonable prices. Love it!",
    "The ambiance is perfect for a romantic dinner.",
    "Fresh ingredients and authentic taste. A must-visit!",
  ];
  
  const neutralTexts = [
    "Food was okay, nothing special but decent.",
    "Average experience. Service could be better.",
    "Good location but prices are a bit high.",
    "Nice place, but the wait time was long.",
  ];
  
  const negativeTexts = [
    "Disappointed with the quality. Won't return.",
    "Too expensive for what you get.",
    "Service was slow and staff seemed uninterested.",
    "Not worth the hype. Food was mediocre.",
  ];

  const positiveKeywords = ['delicious', 'great service', 'fresh', 'authentic', 'recommend'];
  const neutralKeywords = ['okay', 'average', 'decent', 'fine'];
  const negativeKeywords = ['slow service', 'expensive', 'disappointed', 'mediocre'];

  for (let i = 0; i < positive; i++) {
    reviews.push({
      id: `review-pos-${i}`,
      text: positiveTexts[i % positiveTexts.length],
      rating: 4 + Math.random(),
      author: `User ${i + 1}`,
      date: '2024-01-15',
      sentiment: 'positive',
      keywords: [positiveKeywords[i % positiveKeywords.length]],
    });
  }

  for (let i = 0; i < neutral; i++) {
    reviews.push({
      id: `review-neu-${i}`,
      text: neutralTexts[i % neutralTexts.length],
      rating: 3 + Math.random() * 0.5,
      author: `User ${positive + i + 1}`,
      date: '2024-01-10',
      sentiment: 'neutral',
      keywords: [neutralKeywords[i % neutralKeywords.length]],
    });
  }

  for (let i = 0; i < negative; i++) {
    reviews.push({
      id: `review-neg-${i}`,
      text: negativeTexts[i % negativeTexts.length],
      rating: 1 + Math.random() * 1.5,
      author: `User ${positive + neutral + i + 1}`,
      date: '2024-01-05',
      sentiment: 'negative',
      keywords: [negativeKeywords[i % negativeKeywords.length]],
    });
  }

  return reviews;
};

